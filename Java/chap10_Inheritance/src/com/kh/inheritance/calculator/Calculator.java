package com.kh.inheritance.calculator;

public class Calculator {
	//계산기
	int num1, num2;
	//1.더하기
	public int add() { // 파라미터 \ 매개변수 숫자 2개
		return num1 + num2;	
	}
	//2.뺴기 
	public int substruct() { // 파라미터 \ 매개변수 숫자 2개
		return num1 - num2;
	//3.곱하기 
	}
	public int multiply() { // 파라미터 \ 매개변수 숫자 2개
		return num1 * num2;
	}
	//4.나누기
	public int divide() { // 파라미터 \ 매개변수 숫자 2개
		return num1 / num2;
	}
	//나누기에서 몫 이외에 나머지를 보고싶다.
	
	

}
